    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        
        // Instancia de User para tomar los datos
        Tablahb user = new Tablahb(this.txtNombre.getText(), this.txtApellido.getText(), Integer.parseInt(this.txtEdad.getText()));
        
        user.setId(0);
        
        // Envia los datos de User al controlador
        op.altaUsuarios(user);
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
      
        // Instancia de User para tomar los datos
        Tablahb user = new Tablahb(Integer.parseInt(this.txtId.getText()));
        
        user.setNombre(" ");
        user.setApellido(" ");
        user.setEdad(0);
        
        
        // Envia el ID de User al controlador
        op.bajaUsuarios(user);
    }//GEN-LAST:event_btnBorrarActionPerformed

